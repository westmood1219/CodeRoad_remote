#include <stdio.h>
int i;
//全局变量，静态变量都是放在静态区
//全局变量，静态变量不初始化的时候，默认会被初始化为0
//局部变量，是放在栈区，不初始化，默认值是随机值
int main()
{
    i--;
    if (i > sizeof(i)) //sizeof(i)得到的是无符号整型比int空间大,故-1转换成无符号整形变得很大即(11111111111111111111111111111111)
        {
        printf(">\n");
        }
    else
        {
        printf("<\n");
        }
    return 0;
}