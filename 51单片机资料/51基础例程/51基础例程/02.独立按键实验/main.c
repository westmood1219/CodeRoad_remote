#include <reg52.h>

/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/

/*********************************************************************************************************
*                                              位定义
*********************************************************************************************************/
sbit KEY1 = P3^2; //定义按键KEY1
sbit KEY2 = P3^3; //定义按键KEY2
sbit KEY3 = P3^4; //定义按键KEY3
sbit LED1 = P2^4; //定义LED1
sbit LED2 = P2^5; //定义LED2
sbit LED3 = P2^6; //定义LED3

/*********************************************************************************************************
*                                              枚举结构体定义
*********************************************************************************************************/

/*********************************************************************************************************
*                                              内部函数声明
*********************************************************************************************************/
static void DelayNms(int nms);

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/

/*********************************************************************************************************
* 函数名称: DelayNms
* 函数功能: 软件延迟函数，用于延迟一段时间
* 输入参数: xms-延迟时间（毫秒）
* 输出参数: void
* 返 回 值: void
* 创建日期: 2022年2月1日
* 注    意: 
*********************************************************************************************************/
static void DelayNms(int nms)
{
  unsigned int i,j;
  for(i = 0; i < nms; i++)
  {
    for(j = 0; j < 123; j++)
    {
      
    } 
  }
}

/*********************************************************************************************************
* 函数名称: main
* 函数功能: 主函数
* 输入参数: void
* 输出参数: void
* 返 回 值: void
* 创建日期: 2022年2月1日
* 注    意: 
*********************************************************************************************************/
void main()
{
  while(1)  
  {
    if(0 == KEY1)           //第一次检测到KEY1按键被按下
    {
      DelayNms(50);         //等待约50ms后再次检测按键是否被按下，消除按键抖动带来的影响
      if(0 == KEY1)         //第二次检测到KEY1按键被按下
      {
        LED1 = ~LED1;       //对LED1状态取反，改变LED1的亮灭状态
        while(0 == KEY1);   //等待按键弹起  
      }
    }
    if(0 == KEY2)           //第一次检测到KEY2按键被按下
    {                       
      DelayNms(50);         //等待约80ms后再次检测按键是否被按下，消除按键抖动带来的影响
      if(0 == KEY2)         //第二次检测到KEY2按键被按下
      {
        LED2 = ~LED2;       //对LED2状态取反，改变LED2的亮灭状态
        while(0 == KEY2);   //等待按键弹起
      }
    }
    if(0 == KEY3)           //第一次检测到KEY3按键被按下
    {
      DelayNms(50);         //等待约80ms后再次检测按键是否被按下，消除按键抖动带来的影响
      if(0 == KEY3)         //第二次检测到KEY3按键被按下
      {
        LED3 = ~LED3;       //改变LED3的开关状态
        while(0 == KEY3);   //等待按键弹起 
      }
    }
  }
}